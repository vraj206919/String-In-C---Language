#include<stdio.h>

int main()
{
    char letter = 'A';
    printf("The character is: %c\n", letter);
    printf("The ASCII value is: %d\n", letter);

    char LastLetter = 'Z';
    printf("The character is: %c\n", LastLetter);
    printf("The ASCII value is: %d\n", LastLetter);

    char lowercase = 'a';
    printf("The character is: %c\n", lowercase);
    printf("The ASCII value is: %d\n", lowercase);

    char LastLowercase = 'z';
    printf("The character is: %c\n", LastLowercase);
    printf("The ASCII value is: %d\n", LastLowercase);

    return 0;
}


